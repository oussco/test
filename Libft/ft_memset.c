#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*s;
	char	c1;
	size_t	x;

	s = (char *)b;
	c1 = (unsigned char)c;
	x = 0;
	while (x < len)
	{
		s[x] = c1;
		x++;
	}
	return (b);
}
