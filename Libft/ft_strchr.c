#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s1;

	s1 = (char *)s;
	while (*s1 != c)
	{
		if (*s1 == '\0')
			return (NULL);
		s1++;
	}
	return (s1);
}
